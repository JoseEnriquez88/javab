public class Variable {
  public static void main(String[] args) {
    int numero = 5;
    String nombre = "Marina";
    char letra = 'j'; //! en char siempre se utiliza comillas simples en cambio para String se usan comillas dobles
    double tmeperatura = 15.5;
    boolean siONo = false;
    long largo = 123456789;
    System.out.println(numero + nombre + letra + tmeperatura + siONo + largo);
  }
}