package Clase04;

// ! aca se encuentra clase 04 y 05 => IF / ELSE
//! Sentencia IF
public class Clase04 {
  public static void main(String[] args) {
    int num1 = 15;
    int num2 = 10;

    if (num2 > num1) {
      System.out.println("El num2 es mayor a num1");
    } else {
      System.out.println("El num1 es mayor a num2");
    }
  }
}